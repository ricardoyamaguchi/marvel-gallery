import UIKit

extension UIFont {

    // MARK: - Heebo

    static func heeboBlack(size: CGFloat) -> UIFont { UIFont(name: "Heebo-Black", size: size)! }
    static func heeboBold(size: CGFloat) -> UIFont { UIFont(name: "Heebo-Bold", size: size)! }
    static func heeboExtraBold(size: CGFloat) -> UIFont { UIFont(name: "Heebo-ExtraBold", size: size)! }
    static func heeboLight(size: CGFloat) -> UIFont { UIFont(name: "Heebo-Light", size: size)! }
    static func heeboMedium(size: CGFloat) -> UIFont { UIFont(name: "Heebo-Medium", size: size)! }
    static func heeboRegular(size: CGFloat) -> UIFont { UIFont(name: "Heebo-Regular", size: size)! }
    static func heeboThin(size: CGFloat) -> UIFont { UIFont(name: "Heebo-Thin", size: size)! }

    // MARK: - Marvel

    static func marvelRegular(size: CGFloat) -> UIFont { UIFont(name: "Marvel-Regular", size: size)! }

}
